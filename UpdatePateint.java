    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String patientID = txt_id.getText();

        if (!patientID.matches("\\d+")) {

            JOptionPane.showMessageDialog(null, "Please Enter The number");
            return;
        }

        try {
            Connection con = DB.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Patients WHERE PatientID = " + patientID);
            if (rs.next()) {

                txt_Fname.setText(rs.getString("FirstName"));
                txt_Lname.setText(rs.getString("LastName"));
                txt_num.setText(rs.getString("Phone"));
                txt_age.setText(rs.getString("Age"));
                txt_gender.setText(rs.getString("Gender"));
                txt_blood.setText(rs.getString("BloodGroup"));
                txt_add.setText(rs.getString("Address"));
                jTextField8.setText(rs.getString("AnyMajor"));
                txt_id.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(null, "PatientID does not exist");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Connection Error ‚ùå");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String patient = txt_id.getText();
        String Firstname = txt_Fname.getText();
        String Lastname = txt_Lname.getText();
        String Age = txt_age.getText();
        String blood = txt_blood.getText();
        String add = txt_add.getText();
        String gender = txt_gender.getText();
        String Phone = txt_num.getText();
        String anymajor = jTextField8.getText();
        int patientID = Integer.parseInt(patient.trim());
        Connection con = null;
        PreparedStatement ps = null;
        try {
            con = DB.getConnection();
            String sql = "update Patients SET Phone=?,Age=?,FirstName=?,LastName=?,Gender=?,BloodGroup=?,Address=?,AnyMajor=? WHERE PatientID=?";
            ps = con.prepareStatement(sql);

            if (!Age.matches("\\d+")) {

                JOptionPane.showMessageDialog(null, "Please Enter The number in Age");
                return;
            }

            ps.setString(1, Phone);
            ps.setString(2, Age);
            ps.setString(3, Firstname);
            ps.setString(4, Lastname);
            ps.setString(5, gender);
            ps.setString(6, blood);
            ps.setString(7, add);
            ps.setString(8, anymajor);
            ps.setInt(9, patientID);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Updated ‚úÖ");
            new UpdatePateint().setVisible(true);
            setVisible(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "enter data in correct fromate!");
        } finally {
            try {
                if (ps != null) {
                    ps.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

