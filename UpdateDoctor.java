    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String DoctorID = txt_id.getText();

        if (!DoctorID.matches("\\d+")) {

            JOptionPane.showMessageDialog(null, "Please Enter The number");
            return;
        }

        try {
            Connection con = DB.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Doctors WHERE DoctorID = " + DoctorID);
            if (rs.next()) {

                txt_Fname.setText(rs.getString("FirstName"));
                txt_Lname.setText(rs.getString("LastName"));
                txt_num.setText(rs.getString("Phone"));
                txt_age.setText(rs.getString("Age"));
                txt_gender.setText(rs.getString("Gender"));
                txt_add.setText(rs.getString("Address"));
                txt_id.setEditable(false);
                txt_Spec.setText(rs.getNString("Specialest"));

            } else {
                JOptionPane.showMessageDialog(null, "DoctorsID does not exist");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Failed Updated‚ùå " + e.getMessage());

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String Doctors = txt_id.getText();
        String Firstname = txt_Fname.getText();
        String Lastname = txt_Lname.getText();
        String Age = txt_age.getText();
        String add = txt_add.getText();
        String gender = txt_gender.getText();
        String Phone = txt_num.getText();
        String Spec = txt_Spec.getText();

        Connection con = null;
        PreparedStatement ps = null;

        if (!Doctors.matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "‚ùå Please enter numbers only!");
            return;
        }

        int DoctorsId = Integer.parseInt(Doctors.trim());

        try {
            con = DB.getConnection();
            String sql = "UPDATE Doctors SET Phone=?, Age=?, FirstName=?, LastName=?, Gender=?, Address=?, Specialest=? WHERE DoctorID=?";
            ps = con.prepareStatement(sql);

            ps.setString(1, Phone);
            ps.setString(2, Age);
            ps.setString(3, Firstname);
            ps.setString(4, Lastname);
            ps.setString(5, gender);
            ps.setString(6, add);
            ps.setString(7, Spec);
            ps.setInt(8, DoctorsId);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Updated");

        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Enter data in correct format!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

